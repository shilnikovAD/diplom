print(f"  {'Годовой расход прир. газа, млн м³/год':<50} {B_gas_yr:>18.3f}")
print(f"  {'Годовой расход усл. топлива, млн т у.т./год':<50} {B_ref_yr:>18.4f}")
print(HL)
