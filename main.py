print(f"  {'Уд. расход у.т. (брутто), г у.т./кВт·ч':<50} {b_gross:>18.1f}")
print(f"  {'Уд. расход у.т. (нетто), г у.т./кВт·ч':<50} {b_net:>18.1f}")
